public class Fecha {

    private int day;
    private int month;
    private int year;

    public int getDay() {
        return day;
    }

    public void setDay(int day) {

        //This se va a referir a la instancia
        this.day = day;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        //This se va a referir a la instancia
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        //This se va a referir a la instancia
        this.year = year;
    }
}
